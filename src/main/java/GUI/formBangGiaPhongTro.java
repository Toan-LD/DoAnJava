    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int soNguoi = Integer.parseInt(txtSoNguoi.getText());
        int giaTien = Integer.parseInt(txtGiaTien.getText());
        int thongBao = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn xóa?" ,"Thông báo" ,JOptionPane.YES_NO_OPTION);

        if(thongBao == JOptionPane.YES_OPTION) {
            BangGiaPhongTroDTO bangGiaPhongTroDTO = new BangGiaPhongTroDTO();
            bangGiaPhongTroDTO.setSoNguoi(soNguoi);
            bangGiaPhongTroDTO.setGiaTien(giaTien);
            try {
                if(bangGiaBLL.XoaPhong(bangGiaPhongTroDTO) > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                    loadTbl.loadJTable(bangGiaBLL.LoadThongTinGiaThue(), tblBangGia);
                    txtSoNguoi.setText("");
                    txtGiaTien.setText("");
                    btnThemMoi.setEnabled(false);
                    btnCapNhat.setEnabled(false);
                } 
            } catch (Exception ex) {
                Logger.getLogger(formBangGiaPhongTro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

