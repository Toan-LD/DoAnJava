    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        // TODO add your handling code here:
        if(txtSoNguoi.equals("") || txtGiaTien.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Thông tin nhập vào không được để trống");
        } else {
            try {
                BangGiaPhongTroDTO bangGiaPhongTroDTO = new BangGiaPhongTroDTO();
                bangGiaPhongTroDTO.setGiaTien(Integer.parseInt(txtGiaTien.getText()));
                bangGiaPhongTroDTO.setSoNguoi(Integer.parseInt(txtSoNguoi.getText()));
                if(bangGiaBLL.ThemPhong(bangGiaPhongTroDTO) > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm mới phòng thành công");
                    loadTbl.loadJTable(bangGiaBLL.LoadThongTinGiaThue(), tblBangGia);
                    txtSoNguoi.setText("");
                    txtGiaTien.setText("");
                    txtSoNguoi.setEnabled(false);
                    txtGiaTien.setEnabled(false);
                    btnThemMoi.setEnabled(false);
                    btnCapNhat.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Thêm phòng thất bại");
                }
            } catch (Exception ex) {
                Logger.getLogger(formBangGiaPhongTro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnThemMoiActionPerformed

