    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        try{
            KhachThueDTO khachThueDTO = new KhachThueDTO();
            switch (cbbLuaChon.getSelectedIndex()){
                case 0:
                        khachThueDTO.setTenKhach(txtTimKiem.getText());
                        loadtbl.loadJTable(khachthue.TimKhachThueTheoTen(khachThueDTO), tblKhachThue);
                        break;
                case 1:
                        khachThueDTO.setMaKhach(txtTimKiem.getText());
                        loadtbl.loadJTable(khachthue.TimKhachThueTheoMa(khachThueDTO), tblKhachThue);
                        break;
                case 2: 
                        khachThueDTO.setQueQuan(txtTimKiem.getText());
                        loadtbl.loadJTable(khachthue.TimKhachThueTheoQueQuan(khachThueDTO), tblKhachThue);
                        break;
                case 3:
                        khachThueDTO.setNgheNghiep(txtTimKiem.getText());
                        loadtbl.loadJTable(khachthue.TimKhachThueTheoNgheNghiep(khachThueDTO), tblKhachThue);
                        break;
            }
        } catch (Exception ex) {
            Logger.getLogger(formKhachThue.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTimActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        String tenKhach = txtTenKhach.getText();
        String maKhach = txtMaKhach.getText();
        String cmnd = txtCMND.getText();
        int thongBao = JOptionPane.showConfirmDialog(rootPane,"Bạn muốn xóa khách "+ tenKhach + ". Có mã " + maKhach + ".", "Thông báo", JOptionPane.YES_NO_OPTION );
        if(thongBao == JOptionPane.YES_OPTION) {
            KhachThueDTO khachThueDTO = new KhachThueDTO();
            khachThueDTO.setMaKhach(maKhach); //Lấy giá trị mã khách
            khachThueDTO.setCmnd(cmnd);
            if(khachthue.XoaKhachThue(khachThueDTO) > 0){ //nếu xóa thành công
                try{
                  JOptionPane.showMessageDialog(rootPane, "Đã Xóa");
                  loadtbl.loadJTable(khachthue.LoadKhachThue(), tblKhachThue); //load lại bảng khách thuê
                  loadtbl.loadJTable(khachthue.LoadKhachThueDatPhong(), tblKhachThueDatPhong);
                } catch(Exception ex) {
                    Logger.getLogger(formKhachThue.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
        
    }//GEN-LAST:event_btnXoaActionPerformed

