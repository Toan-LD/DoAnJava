    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        if (txtTenKhach.getText().equals("") || txtCMND.getText().equals("") || txtNgheNghiep.getText().equals("") || txtQueQuan.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Thông tin nhập vào không được để trống");
        } else {
            //gán giá trị để sửa khách thuê
            KhachThueDTO khachThueDTO = new KhachThueDTO();
            khachThueDTO.setMaKhach(txtMaKhach.getText());
            khachThueDTO.setTenKhach(txtTenKhach.getText());
            khachThueDTO.setPhai(cbbPhai.getSelectedItem().toString());
            khachThueDTO.setCmnd(txtCMND.getText());
            khachThueDTO.setQueQuan(txtQueQuan.getText());
            khachThueDTO.setNgheNghiep(txtNgheNghiep.getText());
            khachThueDTO.setTaiKhoan(txtTaiKhoan.getText());
            
            if(khachthue.SuaKhachThue(khachThueDTO) > 0) {//Nếu sửa thành công
                try{
                    JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công");
                    loadtbl.loadJTable(khachthue.LoadKhachThue(), tblKhachThue);
                    loadtbl.loadJTable(khachthue.LoadKhachThueDatPhong(), tblKhachThueDatPhong);
                } catch (Exception ex) {
                    Logger.getLogger(formKhachThue.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thất bại");
            }
            txtMaKhach.setText("");
            txtTenKhach.setText("");
            txtCMND.setText("");
            txtTaiKhoan.setText("");
            txtQueQuan.setText("");
            txtNgheNghiep.setText("");
            //txtMaKhach.setText("");
            txtMaKhach.setEnabled(false);
            txtTenKhach.setEnabled(false);
            txtCMND.setEnabled(false);
            txtTaiKhoan.setEnabled(false);
            txtQueQuan.setEnabled(false);
            txtNgheNghiep.setEnabled(false);
            cbbPhai.setEnabled(false);
            btnCapNhat.setEnabled(false);
            btnXoa.setEnabled(false);
            btnSua.setEnabled(false);
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

