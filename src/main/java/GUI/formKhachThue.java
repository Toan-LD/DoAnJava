    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        // TODO add your handling code here:
        if (txtMaKhach.getText().equals("") || txtTenKhach.getText().equals("") || txtCMND.getText().equals("") || txtNgheNghiep.getText().equals("") || txtQueQuan.getText().equals("")) {
            if(rbtnKhachCoDatPhong.isSelected() && txtMatKhau.getText().equals("")||txtTaiKhoan.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Thông tin nhập vào không được để trống");
            return;
            } else {
                JOptionPane.showMessageDialog(rootPane, "Thông tin nhập vào không được để trống");
            }
        }
        
        KhachThueDTO khachThueDTO = new KhachThueDTO();
        khachThueDTO.setMaKhach(txtMaKhach.getText());
        khachThueDTO.setTenKhach(txtTenKhach.getText());
        khachThueDTO.setPhai(cbbPhai.getSelectedItem().toString());
        khachThueDTO.setQueQuan(txtQueQuan.getText());
        khachThueDTO.setNgheNghiep(txtNgheNghiep.getText());
        khachThueDTO.setCmnd(txtCMND.getText());
        khachThueDTO.setTaiKhoan(txtTaiKhoan.getText());
        khachThueDTO.setMatKhau(txtMatKhau.getText());

        if(rbtnKhachCoDatPhong.isSelected()) {
            System.out.println("hehe");
            if(khachthue.ThemKhachThueDatPhong(khachThueDTO) > 0) {
                try {
                    JOptionPane.showMessageDialog(rootPane,"Đã thêm khách thuê thành công");
                    //load lại các table và bỏ check các radiobutton
                    loadtbl.loadJTable(khachthue.LoadKhachThue(), tblKhachThue);
                    loadtbl.loadJTable(khachthue.LoadKhachThueDatPhong(), tblKhachThueDatPhong);
                    rbtnKhachCoDatPhong.setSelected(false);
                    rbtnKhachKhongDatPhong.setSelected(false);
                } catch (Exception ex) {
                    Logger.getLogger(formKhachThue.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Thêm khách thuê thất bại");
            }
        }
        
        if(rbtnKhachKhongDatPhong.isSelected()) {
            System.out.println("huhu");
            if(khachthue.ThemKhachThueKhongDatPhong(khachThueDTO) > 0) {
                try {
                    JOptionPane.showMessageDialog(rootPane, "Thêm khách thuê thành công");
                    loadtbl.loadJTable(khachthue.LoadKhachThue(), tblKhachThue);
                    loadtbl.loadJTable(khachthue.LoadKhachThueDatPhong(), tblKhachThueDatPhong);
                    rbtnKhachCoDatPhong.setSelected(false);
                    rbtnKhachKhongDatPhong.setSelected(false);
                }   catch (Exception ex) {
                    Logger.getLogger(formKhachThue.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        txtMaKhach.setText("");
        txtTenKhach.setText("");
        txtCMND.setText("");
        txtTaiKhoan.setText("");
        txtQueQuan.setText("");
        txtNgheNghiep.setText("");
        txtMatKhau.setText("");
        //txtMaKhach.setText("");
        txtMaKhach.setEnabled(false);
        txtTenKhach.setEnabled(false);
        txtCMND.setEnabled(false);
        txtTaiKhoan.setEnabled(false);
        txtQueQuan.setEnabled(false);
        txtNgheNghiep.setEnabled(false);
        txtMatKhau.setEnabled(false);
        cbbPhai.setEnabled(false);
        btnThemMoi.setEnabled(false);
        
    }//GEN-LAST:event_btnThemMoiActionPerformed

